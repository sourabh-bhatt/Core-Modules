// Template literals are a new feature introduced in ECMAScript 6 (ES6). They are a way to create strings that support interpolation and expressions. This makes them much more powerful than the traditional string literals.
// Template literals are denoted by backticks (`) instead of double or single quotes. They can contain normal text, as well as expressions enclosed in curly braces ({ }). The expressions are evaluated at runtime and their values are substituted into the string.

// For example

const now = "Sourabh";
const template = `My name is: ${now}`;
console.log(template);
